    private void mostrar(){
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = bd.getTabla("SELECT * FROM users");
        modelo.setColumnIdentifiers(new Object[]{"Id","Usuario","Password","Nombre","Correo","Id_Tipo"});
        try{
            while ( rs.next()){
                modelo.addRow(new Object[]{rs.getString("id"),rs.getString("usuario")
                ,rs.getString("password"), rs.getString("nombre"), rs.getString("correo"), rs.getString("id_tipo")
                });
            }
            tabla.setModel(modelo);
        }catch(Exception e){
            System.out.println(e);
        }
    }

