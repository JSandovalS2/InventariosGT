    private void mostrar(){
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = bd.getTabla("SELECT * FROM client");
        modelo.setColumnIdentifiers(new Object[]{"Id","Nombres","Apellidos","Direccion","Telefono","nit"});
        try{
            while ( rs.next()){
                modelo.addRow(new Object[]{rs.getString("idC"),rs.getString("Nombres")
                ,rs.getString("Apellidos"), rs.getString("Direccion"), rs.getString("Telefono"), rs.getString("nit")
                });
            }
            tabla.setModel(modelo);
        }catch(Exception e){
            System.out.println(e);
        }
    }
